//. SWITCH

let environment = "UAT";

switch (environment) {
  case "QA":
    console.log("www.QA.com");

    break;

  case "DEV":
    console.log("www.DEV.com");

    break;

  case "UAT":
    console.log("www.UAT.com");

    break;

  case "PROD":
    console.log("www.PROD.com");

    break;

  default:
    console.log("Invalid Environment");
    break;
}

//.  FOR LOOP

for (let i = 1; i <= 3; i++) {
  console.log(i);
  console.log("Nithin");
}

//. WHILE LOOP

// initialization
// while (condition) {
// block of code
// increment/decrement
// }

let i = 10;

while (i >= 5) {
  console.log(i);
  console.log("Nikki");
  i--;
}

//. DO WHILE LOOP

let j = 4;

do {
  console.log(j);
  console.log("Chikki");
  j++;
} while (j <= 3);

